#A hardware manufacturer produces nuts and bolts. Each bolt produced is attached to a nut to
#make a single unit. It is known that 2% of the nuts produced and 3% of the bolts produced are
#defective in some way. A nut-bolt unit is considered defective if either the nut or the bolt has a
#defect.
#(a) Determine the percentage of defective nut-bolt units
#(b) What assumptions are you making in solving part (a)?

# Solution
# (a) The probability of a nut being defective is 0.02. The probability of a bolt being defective is 0.03.
# The probability of a nut-bolt unit being defective is 0.02 + 0.03 - 0.02*0.03 = 0.029.
# (b) The assumption is that the nuts and bolts are independent.
