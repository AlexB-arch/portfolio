#7. A person guesses the answers to three multiple choice questions at random. If there are five
#possible choices on each question, what is the probability that
#(a) all three are answered correctly?
#(b) at least two questions are answered correctly?
#(c) all three questions are answered correctly given that at least one question is answered
#correctly?

# Solution
# (a) The probability of answering all three questions correctly is 1/5 * 1/5 * 1/5 = 1/125.
# (b) The probability of answering at least two questions correctly is 1 - (4/5)^3 = 1 - 0.512 = 0.488.
# (c) The probability of answering all three questions correctly given that at least one question is
# answered correctly is 1/5 * 1/5 * 1/5 / (1 - (4/5)^3) = 1/125 / 0.488 = 1/61.

